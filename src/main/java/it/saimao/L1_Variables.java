package it.saimao;

public class L1_Variables {
    public static void main(String[] args) {
        // Variables
        int myNumberIsOneThousandAndTwoHundreds = 1234;

        String name;
        name = "Sai Sai";
        System.out.println(name);

        // Literals
        System.out.println("Hello World"); // String literal
        System.out.println(1234); // integer literal
        System.out.println(true); // boolean literal
        System.out.println('a'); // char literal
    }
}
