package it.saimao;

public class L12_AssignmentOperators {
    public static void main(String[] args) {

        int i = 10;
        System.out.println(i += 10); // i = i + 10;
        System.out.println(i -= 5); // i = i - 5;
        System.out.println(i *= 2); // i = i * 2;
        System.out.println(i /= 10); // i = i / 10;
        System.out.println(i %= 2); // i = i % 2;

    }
}
