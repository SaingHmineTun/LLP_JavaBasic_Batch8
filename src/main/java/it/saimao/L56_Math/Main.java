package it.saimao.L56_Math;

public class Main {
    public static void main(String[] args) {

        System.out.println(Math.abs(-12)); // absolute
        System.out.println(Math.floor(13.99999)); // 13.0
        System.out.println(Math.ceil(13.00001)); // 14.0
        System.out.println(Math.round(13.49)); // .49 = floor/ .50 = ceil
        System.out.println(Math.PI);

    }
}
